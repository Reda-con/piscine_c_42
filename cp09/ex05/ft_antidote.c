int		ft_antidote(int i, int j, int k)
{
	if (i == j || j == k || i == k)
		return (0);
	if ((i > j && i < k) || (i < j && i > k))
		return (i);
	else if ((j < k && j > i) || (j > k && j < i))
		return (j);
	else
		return (k);
}
