void	ft_putchar(char c);

int		main(int arc, char **arv)
{
	int		i;

	i = 0;
	if (arc > 0)
	{
		while (arv[0][i])
		{
			ft_putchar(arv[0][i]);
			i++;
		}
	}
	ft_putchar('\n');
	return (0);
}
