void	ft_putchar(char c);

int		main(int ac, char **av)
{
	int		i;
	int		j;

	j = ac;
	if (ac > 1)
	{
		while (j > 1)
		{
			i = 0;
			while (av[j - 1][i])
			{
				ft_putchar(av[j - 1][i]);
				i++;
			}
			ft_putchar('\n');
			j--;
		}
	}
	return (0);
}
