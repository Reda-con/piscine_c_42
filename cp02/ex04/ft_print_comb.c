void	ft_putchar(char c);

void	ft_print_comb(void)
{
	int	t[3];

	t[0] = 0;
	while (t[0] <= 7)
	{
		t[1] = t[0] + 1;
		while (t[1] <= 8)
		{
			t[2] = t[1] + 1;
			while (t[2] <= 9)
			{
				ft_putchar(t[0] + 48);
				ft_putchar(t[1] + 48);
				ft_putchar(t[2] + 48);
				if (t[0] != 7)
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
				t[2]++;
			}
			t[1]++;
		}
		t[0]++;
	}
}
